package com.sharayu.programs;

public class TryFor {
	public static void main(String[] args) {
		int i;
		
		for(i=1;i<=10;i++)
			System.out.println("welcome to java "+i);
		
		System.out.println("---------------------");
		
		for(i=51;i<=75;i+=3)
			System.out.println("thanks "+i);
		
		System.out.println("-------------------");
		
		for(i=10;i>=1;i--)
			System.out.println(i);
		
		
	}

}
